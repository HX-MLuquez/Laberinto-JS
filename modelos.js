function invertPacman(obj) {
  let inverted = { ...obj, lab: {} };

  for (let key in obj.lab) {
      if (Array.isArray(obj.lab[key])) {
          inverted.lab[key] = obj.lab[key].map(cell => {
              if (Array.isArray(cell) && cell.length === 0) return "";
              if (cell === 73 || (typeof cell === "object" && !Array.isArray(cell) && Object.keys(cell).length === 0)) return cell;
              return [];
          });
      } else {
          inverted.lab[key] = obj.lab[key];
      }
  }

  return inverted;
}

